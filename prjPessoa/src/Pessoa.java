public class Pessoa
{
    // Atributos privados
    private String nome;
    private int idade;

    // Getter e Setter para nome
    public String getNome()
    {
        return nome;
    }

    public void setNome(String nome)
    {
        this.nome = nome;
    }

    // Getter e Setter para idade
    public int getIdade()
    {
        return idade;
    }

    public void setIdade(int idade)
    {
        this.idade = idade;
    }
}